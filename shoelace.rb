

Find shoe with laces.  Set shoe on floor with you directly behind the open shoe.
Put foot in shoe.  Use your two hands to hold the two laces, left in left, right in right.
Cross laces so the left is on the right and the right is on the left.
Using your left hand (i'm left handed) wrap the left lace over the right lace
coming between your body and the lace and over the right lace.
Then pull the lace underneath the right lace, coming towards your body.
Pull tight.
Create two elongated loops of string, one left loop and one right loop with each only using their respective lace.
Now, do the exact same looping motion of the left lace going around the right lace except with the loops.
Pull tight.
